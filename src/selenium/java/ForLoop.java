package selenium.java;

public class ForLoop {

	public static void printNumber() {

		// Print 1 to 10

		//For Loop: Checks the condition then increment or decrement
		for (int i = 1; i <= 10; i++) {
			System.out.println((+i));
		}

	}

	public static void printNumber1() {

		// Print -1 to -10

		for (int i = -1; i >= -10; i--) {
			System.out.println((+i));
		}

	}
}
