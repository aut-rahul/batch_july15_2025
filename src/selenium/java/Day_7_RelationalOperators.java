package selenium.java;

public class Day_7_RelationalOperators {

	public static void main(String[] args) {

		//Relational or Comparison Operators: ==, !=, >, <, >=, <=
		// Gives the result in true or false
		
		int a=10;
		int b=20;
		System.out.println("a="+a+ " b="+b);
		System.out.println("a equal to b: "+(a==b)); // false
		System.out.println("a not equal to b: "+(a!=b)); // true
		System.out.println("a greater than b: "+(a>b)); //false
		System.out.println("a less than b: "+(a<b)); // true
		System.out.println("a is equal or greater than b: "+(a>=b)); // false
		System.out.println("a is equal or less than b: "+(a<=b)); // true
		

	}

}
