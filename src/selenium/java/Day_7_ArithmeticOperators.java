package selenium.java;

public class Day_7_ArithmeticOperators {

	public static void main(String[] args) {
		
		//Arithmetic Operators: +, -, *, /, %(Modulus)
		
		int a=10;
		int b=2;
		
		System.out.println("sum of a and b is "+(a+b)); //12
		System.out.println("substract of b from a is "+(a-b)); //8
		System.out.println("substract of a from b is "+(b-a)); //-8
		System.out.println("multiply of a from b is "+(a*b)); //20
		System.out.println("divide of a from b is "+(a/b)); // 5
		System.out.println("remainder of a divide by b is "+(a*b)); //0

	}

}
